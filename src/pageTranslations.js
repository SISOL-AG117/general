const reverseTranslations = Object.fromEntries(
  Object.entries(pageTranslations).map(([spanish, english]) => [english, spanish]),
)

const normalize = (value) => value.trim().replace(/\s+/g, ' ')

export function translateValue(value, language) {
  const normalized = normalize(value)
  if (!normalized) return value

  let spanish = reverseTranslations[normalized] || normalized
  const englishPatterns = [
    [/^Building ([A-E]) · Level (.+)$/, (_, tower, level) => `Torre ${tower} · Nivel ${level}`],
    [/^Building ([A-E])$/, (_, tower) => `Torre ${tower}`],
    [/^(\d+) residences available$/, (_, count) => `${count} residencias disponibles`],
    [/^(\d+) bed\. · ([\d.]+) baths( · study)?$/, (_, beds, baths, study) => `${beds} rec. · ${baths} baños${study ? ' · estudio' : ''}`],
    [/^Floor plan for (.+)$/, (_, type) => `Plano de ${reverseTranslations[type] || type}`],
  ]

  for (const [pattern, formatter] of englishPatterns) {
    const match = spanish.match(pattern)
    if (match) {
      spanish = formatter(...match)
      break
    }
  }

  let translated = language === 'en' ? pageTranslations[spanish] || spanish : spanish

  const patterns = [
    [/^Torre ([A-E]) · Nivel (.+)$/, (_, tower, level) => language === 'en' ? `Building ${tower} · Level ${level}` : `Torre ${tower} · Nivel ${level}`],
    [/^Torre ([A-E])$/, (_, tower) => language === 'en' ? `Building ${tower}` : `Torre ${tower}`],
    [/^(\d+) residencias disponibles$/, (_, count) => language === 'en' ? `${count} residences available` : `${count} residencias disponibles`],
    [/^(\d+) rec\. · ([\d.]+) baños( · estudio)?$/, (_, beds, baths, study) => language === 'en' ? `${beds} bed. · ${baths} baths${study ? ' · study' : ''}` : `${beds} rec. · ${baths} baños${study ? ' · estudio' : ''}`],
    [/^Plano de (.+)$/, (_, type) => language === 'en' ? `Floor plan for ${pageTranslations[type] || type}` : `Plano de ${reverseTranslations[type] || type}`],
  ]

  for (const [pattern, formatter] of patterns) {
    const match = spanish.match(pattern)
    if (match) {
      translated = formatter(...match)
      break
    }
  }

  const leading = value.match(/^\s*/)?.[0] || ''
  const trailing = value.match(/\s*$/)?.[0] || ''
  return `${leading}${translated}${trailing}`
}

export function translateDocument(root, language) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT)
  const textNodes = []
  while (walker.nextNode()) textNodes.push(walker.currentNode)

  textNodes.forEach((node) => {
    const nextValue = translateValue(node.nodeValue, language)
    if (node.nodeValue !== nextValue) node.nodeValue = nextValue
  })

  root.querySelectorAll?.('[alt], [aria-label], [placeholder], [title], [data-label]').forEach((element) => {
    ['alt', 'aria-label', 'placeholder', 'title', 'data-label'].forEach((attribute) => {
      const value = element.getAttribute(attribute)
      if (!value) return
      const nextValue = translateValue(value, language).trim()
      if (value !== nextValue) element.setAttribute(attribute, nextValue)
    })
  })
}
